package juego;

public class Enemigo {

    private int x, y, vida;

    public Enemigo(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getVida() {
        return vida;
    }
}
